"""NLP TSTR — train on synthetic sentiment, test on a REAL public dataset.

Train a text classifier ONLY on DataGen's NLP classification output (synthetic
product-review sentiment, offline engine), then test it on the real, public
**UCI Sentiment Labelled Sentences** corpus (3,000 human-written amazon / imdb /
yelp sentences, binary labels). The model never sees a real sentence during
training, so this measures genuine transfer.

The dataset is downloaded once and cached under benchmark/.data/. If the network
is unavailable, the script falls back to a small built-in set of human-written
reviews so it still runs (clearly flagged in the output).

The offline engine is the always-available lower bound. When OPENROUTER_API_KEY
is set, the script ALSO measures the LLM ceiling: the exact same pipeline, but
the example pool is generated by the OpenRouter multi-model fallback chain (the
one the live app uses) instead of the curated offline banks. The extra run is
reported under "llm_ceiling" — everything else is unchanged, so the only moving
part is where the synthetic text comes from.

Run from backend/:  python -m benchmark.nlp_tstr_benchmark
Depends on scikit-learn (already in the project env).
"""

import io
import json
import os
import urllib.request
import zipfile

import numpy as np
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, f1_score
from sklearn.pipeline import make_pipeline

from nlp_tasks import POOL_KEYS, build_nlp_prompt, build_offline_pool, generate_nlp_dataset

SEED = 42
LABELS = ["positive", "negative"]

# LLM ceiling run: how large a pool to gather and how to batch the API calls.
# The pool is deduplicated across batches, so several calls cover a varied set.
# Target matches the offline pool (400) for a like-for-like comparison; the
# extra batch headroom absorbs cross-batch duplicates.
LLM_POOL_TARGET = 400
LLM_BATCH = 60
LLM_MAX_BATCHES = 10

# The real test corpus is Amazon/IMDB/Yelp sentences, so the LLM run describes
# that domain explicitly — this measures the ceiling "when the distribution is
# described well." The offline run stays generic, so llm_ceiling is best-case.
LLM_DOMAIN = (
    "short consumer reviews like those written on Amazon, IMDB, and Yelp: "
    "product reviews (electronics, books, household goods), movie reviews, and "
    "restaurant reviews. Use natural, varied, conversational wording."
)
DATA_DIR = os.path.join(os.path.dirname(__file__), ".data")
UCI_URL = ("https://archive.ics.uci.edu/ml/machine-learning-databases/"
           "00331/sentiment%20labelled%20sentences.zip")

# Offline fallback: human-written reviews (only used if the download fails).
_FALLBACK = [
    ("This blender is fantastic, easily worth every cent.", "positive"),
    ("Absolutely love these running shoes, so comfortable.", "positive"),
    ("The headphones work perfectly and the sound is great.", "positive"),
    ("Brilliant little camera, I would recommend it to anyone.", "positive"),
    ("The desk lamp broke on the second day, terrible quality.", "negative"),
    ("Cheap plastic that fell apart almost immediately.", "negative"),
    ("My tablet stopped working after a week, total waste.", "negative"),
    ("Flimsy build and overpriced, I regret buying it.", "negative"),
]


def load_real_test():
    """Return (texts, labels, source). UCI corpus if reachable, else fallback."""
    try:
        os.makedirs(DATA_DIR, exist_ok=True)
        zpath = os.path.join(DATA_DIR, "uci_sentiment.zip")
        if not os.path.exists(zpath):
            urllib.request.urlretrieve(UCI_URL, zpath)
        texts, labels = [], []
        with zipfile.ZipFile(zpath) as z:
            for fname in z.namelist():
                if fname.endswith("_labelled.txt"):
                    for line in io.TextIOWrapper(z.open(fname), encoding="utf-8", errors="ignore"):
                        line = line.strip()
                        if not line or "\t" not in line:
                            continue
                        text, _, lab = line.rpartition("\t")
                        lab = lab.strip()
                        if lab in ("0", "1"):
                            texts.append(text.strip())
                            labels.append("positive" if lab == "1" else "negative")
        if len(texts) >= 100:
            return texts, labels, f"UCI Sentiment Labelled Sentences ({len(texts)} real sentences)"
    except Exception as e:  # noqa: BLE001 — any network/parse failure → fallback
        print(f"[warn] UCI download failed ({e.__class__.__name__}); using built-in fallback set.")
    return ([t for t, _ in _FALLBACK], [l for _, l in _FALLBACK],
            f"built-in human-written fallback ({len(_FALLBACK)} sentences)")


def datagen_training_set(n_rows, distorted=False, seed=SEED, pool=None):
    """Build a synthetic training set. With no pool, uses the offline engine;
    pass an LLM-generated pool to measure the LLM ceiling on the same pipeline."""
    if pool is None:
        pool = build_offline_pool("classification", 400, LABELS, seed)
    rows, report = generate_nlp_dataset(
        "classification", n_rows, distorted=distorted, seed=seed, pool=pool, labels=LABELS,
    )
    return [r["Text"] for r in rows], [r["Label"] for r in rows], report


def llm_example_pool(target=LLM_POOL_TARGET, labels=LABELS):
    """Gather a classification example pool from the OpenRouter LLM fallback
    chain — the same prompt and multi-model chain the live app uses.

    Returns (pool, model_used), or (None, None) when no API key is set or the
    LLM path is unusable, so the caller can cleanly fall back to offline-only.
    """
    api_key = os.environ.get("OPENROUTER_API_KEY")
    if not api_key:
        return None, None
    try:
        # On machines behind a TLS-intercepting proxy/AV, httpx (via certifi)
        # can't verify OpenRouter's cert. Route SSL through the OS trust store
        # when truststore is available; harmless and skipped if it isn't.
        import truststore
        truststore.inject_into_ssl()
    except ImportError:
        pass
    try:
        # Imported lazily so the offline run never depends on the web stack.
        from openai import OpenAI

        from app import fetch_record_pool
    except Exception as e:  # noqa: BLE001 — missing dep / import error → skip ceiling
        print(f"[warn] LLM path unavailable ({e.__class__.__name__}); skipping ceiling run.")
        return None, None

    client = OpenAI(base_url="https://openrouter.ai/api/v1", api_key=api_key)
    pseudo_columns = [{"name": k} for k in POOL_KEYS["classification"]]
    pool, seen, model_used = [], set(), None
    for _ in range(LLM_MAX_BATCHES):
        if len(pool) >= target:
            break
        prompt = build_nlp_prompt("classification", LLM_BATCH, labels, LLM_DOMAIN)
        records, model = fetch_record_pool(client, prompt, pseudo_columns)
        if not records:
            break  # whole chain failed/unreachable — stop, fall back to offline
        model_used = model_used or model
        for rec in records:
            key = rec["text"].strip().lower()
            if key and key not in seen:
                seen.add(key)
                pool.append(rec)

    if len(pool) < 20:
        print(f"[warn] LLM produced only {len(pool)} usable examples; skipping ceiling run.")
        return None, None
    return pool[:target], model_used


def _fit(X, y):
    clf = make_pipeline(
        TfidfVectorizer(ngram_range=(1, 2), min_df=2, sublinear_tf=True),
        LogisticRegression(max_iter=2000, C=4.0),
    )
    clf.fit(X, y)
    return clf


def _evaluate(pool, X_real, y_real, rng):
    """Train on a synthetic set (offline or LLM pool), test on the real corpus.
    Returns the accuracy, macro-F1, shuffled control, and exactness reports."""
    X_train, y_train, train_report = datagen_training_set(1500, distorted=False, pool=pool)
    pred = _fit(X_train, y_train).predict(X_real)
    pred_ctrl = _fit(X_train, list(rng.permutation(y_train))).predict(X_real)
    _, _, distorted_report = datagen_training_set(1500, distorted=True, pool=pool)
    return {
        "accuracy": round(float(accuracy_score(y_real, pred)), 4),
        "macro_f1": round(float(f1_score(y_real, pred, average="macro")), 4),
        "shuffled_control_accuracy": round(float(accuracy_score(y_real, pred_ctrl)), 4),
        "train_report": train_report,
        "distorted_report": distorted_report,
    }


def main():
    rng = np.random.default_rng(SEED)
    X_real, y_real, source = load_real_test()
    _, counts = np.unique(y_real, return_counts=True)
    majority = float(counts.max() / counts.sum())

    # ── Lower bound: offline engine (no API key required, always runs) ──
    off = _evaluate(None, X_real, y_real, rng)

    report = {
        "task": "classification (sentiment, binary)",
        "train": "DataGen synthetic, 1500 rows (offline engine)",
        "test": source,
        "model": "TF-IDF (1-2gram) + LogisticRegression",
        "majority_class_baseline": round(majority, 4),
        "tstr": {
            "synthetic_to_real_accuracy": off["accuracy"],
            "synthetic_to_real_macro_f1": off["macro_f1"],
        },
        "shuffled_control_accuracy": off["shuffled_control_accuracy"],
        "controls": {
            "balanced_class_distribution": off["train_report"]["label_distribution"],
            "distorted_class_distribution": off["distorted_report"]["label_distribution"],
            "distorted_label_noise_flips": off["distorted_report"]["label_noise_flips"],
        },
    }

    # ── Ceiling: identical pipeline, example pool from the OpenRouter LLM ──
    pool, model_used = llm_example_pool()
    if pool:
        llm = _evaluate(pool, X_real, y_real, rng)
        report["llm_ceiling"] = {
            "train": f"DataGen synthetic, 1500 rows (OpenRouter LLM pool, {len(pool)} examples)",
            "model_used": model_used,
            "prompt_domain": "domain-matched (Amazon/IMDB/Yelp-style reviews)",
            "synthetic_to_real_accuracy": llm["accuracy"],
            "synthetic_to_real_macro_f1": llm["macro_f1"],
            "shuffled_control_accuracy": llm["shuffled_control_accuracy"],
            "gain_vs_offline": round(llm["accuracy"] - off["accuracy"], 4),
        }
    else:
        report["llm_ceiling"] = (
            "skipped — set OPENROUTER_API_KEY to measure the LLM text-transfer ceiling"
        )

    print(json.dumps(report, indent=2))
    return report


if __name__ == "__main__":
    main()
